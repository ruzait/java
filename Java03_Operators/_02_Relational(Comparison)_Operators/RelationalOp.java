public class RelationalOp {

    public static void main(String[] args) {

        // used to compare two values (or variables)
        // return value of a comparison is either true or false

        int a = 5;
        int b = 10;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}