public class assingnmentOp {

    public static void main(String[] args) {
        
        // Assignment operators are used to assign values to variables.
        
        /* Simple Assignment Operator => left side consists of the operand and the right side consists of a value */
        int x = 5;
        System.out.println(x);


        /* Compound Assignment Operator => used where +,-,*,/ is used along with the = operator */
        x += 3;     //  Same As x = x + 3
        System.out.println(x);

        x -= 6;     //  Same As x = x - 6
        System.out.println(x);



    }
    
}